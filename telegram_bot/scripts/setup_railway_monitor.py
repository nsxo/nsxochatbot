def test_railway_connection(credentials):
    """Test the Railway API connection."""
    print("\nğŸ§ª Testing Railway API connection...")
    
    headers = {
        "Authorization": f"Bearer {credentials['RAILWAY_TOKEN']}",
        "Content-Type": "application/json"
    }
    
    query = """
    query GetService($serviceId: String!) {
        service(id: $serviceId) {
            id
            name
            latestDeployment {
                status
                createdAt
            }
        }
    }
    """
    
    variables = {"serviceId": credentials['RAILWAY_SERVICE_ID']}
    
    try:
        response = requests.post(
            "https://backboard.railway.app/graphql",
            json={"query": query, "variables": variables},
            headers=headers,
            timeout=30
        )
        
        if response.status_code == 200:
            data = response.json()
            if 'data' in data and 'service' in data['data'] and data['data']['service']:
                service = data['data']['service']
                print("âœ… Railway API connection successful!")
                print(f"ğŸ“‹ Service: {service['name']}")
                
                if service.get('latestDeployment'):
                    deployment = service['latestDeployment']
                    print(f"ğŸš€ Latest deployment: {deployment['status']}")
                    print(f"ğŸ“… Created: {deployment['createdAt']}")
                
                return True
            else:
                print("âŒ Service not found. Check your Service ID.")
                return False
        else:
            print(f"âŒ API request failed: {response.status_code}")
            print(f"Response: {response.text}")
            return False
            
    except Exception as e:
        print(f"âŒ Connection test failed: {e}")
        return False

def save_credentials(credentials):
    """Save credentials to environment file."""
    env_file = Path(".env")
    
    # Read existing .env if it exists
    existing_vars = {}
    if env_file.exists():
        with open(env_file, 'r') as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith('#') and '=' in line:
                    key, value = line.split('=', 1)
                    existing_vars[key] = value
    
    # Update with new credentials
    existing_vars.update(credentials)
    
    # Write back to .env
    with open(env_file, 'w') as f:
        f.write("# Railway Deployment Monitor Configuration\n")
        f.write("# Generated by setup_railway_monitor.py\n\n")
        
        for key, value in existing_vars.items():
            f.write(f"{key}={value}\n")
    
    print(f"âœ… Credentials saved to {env_file}")

